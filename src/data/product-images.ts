export function newsImagePath(filename: string): string {
  return `${NEWS_IMAGE_DIR}/${encodeURIComponent(filename)}`
}

export const NEWS_PRODUCT_IMAGES: string[] = NEWS_IMAGE_FILES.map(newsImagePath)

export function getNewsImageAt(index: number): string {
  return NEWS_PRODUCT_IMAGES[index % NEWS_PRODUCT_IMAGES.length]
}

export function getNewsImagesForProduct(index: number, count = 1): string[] {
  const images: string[] = []
  for (let i = 0; i < count; i++) {
    images.push(getNewsImageAt(index + i))
  }
  return images
}

export function assignNewsImagesToProducts<T extends { images: string[] }>(
  items: T[]
): T[] {
  return items.map((product, index) => ({
    ...product,
    images: getNewsImagesForProduct(
      index,
      Math.min(product.images.length, 2) || 1
    ),
  }))
}
